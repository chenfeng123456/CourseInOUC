void main()
{
  unsigned int j=0,num;
  EA = 1;     //开总中断
  EX0 = 1;    //开外部中断0
  EX1 = 1;
  IT0 = 1;    //选择电平负跳变触发方式，
  IT1 = 1; 
	while(1)
	{
		num=9;
		while(num--)
		{
		    if (lie_flag == 0)
			{
			    display_hang(j);
		    }
			else if(hang_flag == 0)
			{
			    display_lie(j);
			}
			delay(10);

	    }
		if ( (hang_flag==1&&lie_flag==0) || (lie_flag == 1 && hang_flag == 0) )
		{
		    j=j+2;
	    }
		else if ( (hang_flag == -1 && lie_flag == 0) || (lie_flag == -1 && hang_flag == 0) )
		{
		    j = j - 2;
			if(j>270)
			    j=270;
		}
		
		
	}
}
	  
void send_data_L_hang(unsigned int dat)
{
    unsigned char i;
    
    for(i=0;i<8;i++)
    {
        LSH = 0;
        LDS = dat&0x80;       //temp<<=1;
        dat = dat<<1;               //ds = CY;
        LSH = 1;
    }
		LST = 0;
    _nop_();
    _nop_();
    LST = 1;
}
 
void send_data_H_hang(unsigned int dat)
{
    int i; 
    for(i=0;i<16;i++)
    {
			  
        HSH = 0;
        HDS = dat&0x8000;
        dat =dat<<1;
        HSH = 1;
    }	
		HST = 0;
    _nop_();
    HST = 1;
}
void delay(unsigned int m)
{
    unsigned char i;
    for(;m>0;m--)
    {
        for(i=0;i<124;i++)
			{}
    }
}
 
void display_hang( int a)
{
    unsigned char i;
	unsigned int x;
    for(i=0;i<16;i++)
    {
        send_data_H_hang(sel[i]);
		x=a+2*i;//展示第几个字符
		if(a+2*i>270)
			x=(a+2*i)%272;
		send_data_L_hang(hanzi_hang[x]);
		send_data_L_hang(hanzi_hang[x+1]);
		delay(1);	  
    }
}


